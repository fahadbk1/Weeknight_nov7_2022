package Day3_10172022;

public class T2_IfElseConditions {
    public static void main(String[] args) {
        //declare two integer variables to verify if a is less than b, then print the result
        int a = 5;
        int b = 3;

        if (a < b) {
            System.out.println("a is less than b");
        } else {
            System.out.println("a is either greater or equal to b");
        }
    }//end of main
}//end of class
